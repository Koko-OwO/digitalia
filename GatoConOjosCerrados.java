public class GatoConOjosCerrados {
    public static void main(String[] args) {
        imprimirGato();
        cerrarOjos();
        imprimirGato();
    }

    public static void imprimirGato() {
        System.out.println(" /\\_/\\ ");
        System.out.println("( o.o )");
        System.out.println(" > ^ < ");
    }

    public static void cerrarOjos() {
        System.out.println(" /\\_/\\ ");
        System.out.println("( -.- )");
        System.out.println(" > ^ < ");
    }
}
